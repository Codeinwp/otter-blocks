/**
 * WordPress dependencies
 */
import { __ } from '@wordpress/i18n';

import {
	RichText,
	useBlockProps
} from '@wordpress/block-editor';

import {
	Fragment,
	useEffect
} from '@wordpress/element';

/**
 * Internal dependencies
 */
import metadata from './block.json';
import { blockInit } from '../../../helpers/block-utility.js';
import Inspector from './inspector.js';
import { _cssBlock } from '../../../helpers/helper-functions';

const { attributes: defaultAttributes } = metadata;

/**
 * Form Textarea component
 * @param {import('./types').FormTextareaProps} props
 * @return
 */
const Edit = ({
	attributes,
	setAttributes,
	clientId
}) => {
	useEffect( () => {
		const unsubscribe = blockInit( clientId, defaultAttributes );
		return () => unsubscribe( attributes.id );
	}, [ attributes.id ]);

	const blockProps = useBlockProps();

	return (
		<Fragment>
			<Inspector
				attributes={ attributes }
				setAttributes={ setAttributes }
				clientId={ clientId }
			/>

			<div { ...blockProps }>
				<style>
					{
						`#block-${clientId}` + _cssBlock([
							[ '--label-color', attributes.labelColor ]
						])
					}
				</style>
				<label
					htmlFor={ attributes.id ? attributes.id + '-input' : '' }
					className="otter-form-textarea-label"
				>
					<RichText
						placeholder={ __( 'Type hereâ€¦', 'otter-blocks' ) }
						className="otter-form-textarea-label__label"
						value={ attributes.label }
						onChange={ label => setAttributes({ label }) }
						tagName="span"
					/>

					{ attributes.isRequired && (
						<span className="required">*</span>
					) }
				</label>

				<textarea
					placeholder={ attributes.placeholder }
					name={ attributes.id }
					id={ attributes.id ? attributes.id + '-input' : '' }
					required={ attributes.isRequired }
					rows={ 10 }
					className="otter-form-textarea-input components-textarea-control__input"
					onChange={ e => {
						setAttributes({ defaultValue: e.target.value });
					} }
					value={ attributes.defaultValue }
				>
				</textarea>
				{
					attributes.helpText && (
						<span
							className="o-form-help"
						>
							{ attributes.helpText }
						</span>
					)
				}
			</div>
		</Fragment>
	);
};

export default Edit;
